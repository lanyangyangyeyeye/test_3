package cn.lookout.base.bean;

public class DtDeviceSwitchBean {

	private String id;
	
	private String deviceId;
	
	private String switchNum;
	
	private String name;
	
	private String displayName;
	
	private String gfNum;

	public String getGfNum() {
		return gfNum;
	}

	public void setGfNum(String gfNum) {
		this.gfNum = gfNum;
	}

	public String getDisplayName() {
		return displayName;
	}

	public void setDisplayName(String displayName) {
		this.displayName = displayName;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getDeviceId() {
		return deviceId;
	}

	public void setDeviceId(String deviceId) {
		this.deviceId = deviceId;
	}

	public String getSwitchNum() {
		return switchNum;
	}

	public void setSwitchNum(String switchNum) {
		this.switchNum = switchNum;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}
